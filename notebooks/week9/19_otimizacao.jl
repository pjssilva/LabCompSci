Um problema de otimiza√ß√£o como
``\min_{b,m}``  `loss(b, m)`
√© um exemplo bastande "pesado" de uma fun√ß√£o de fun√ß√£o. Aqui estamos falando que a entrada do processo de otimiza√ß√£o √© uma *fun√ß√£o* em si  `loss(b, m)` . J√° a sa√≠da √© um objeto mais simples: no caso a localiz√ß√£o do m√≠nimo. J√° usamos o termo "pesado" para indicar que potencialmente muita computa√ß√£o, muitos c√°lculos, ser√£o necess√°rios para executar a tarefa.

