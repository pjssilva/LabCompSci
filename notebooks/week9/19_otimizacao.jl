Um primeiro m√©todo que se desenvolve em otimiza√ß√£o √© chamado descida de gradiente. Nele os passos s√£o tomados na dire√ß√£o de menos o gradiente, j√° que essa √© a dire√ß√£o de descida. √â a dire√ß√£o natural que uma bola tomaria ao ser solta num gr√°fico de uma fun√ß√£o sob a a√ß√£o da gravidade. Voc√™ pode imaginar o m√©todo de descida de gradiente como uma implementa√ß√£o desse processo din√¢mico por um m√©todo de primeira ordem do tipo Euler. 

Com o tempo a comunidade de otimiza√ß√£o desenvolveu diversas t√©cnicas mais sofisticadas que buscavam usar informa√ß√£o de segunda orde. Mas na verdade eles s√≥ estavam tentando imitar o que Newton j√° tinha feito muitos anos antes. A novidade? Como fazer ou aproximar Newton sem pagar o pre√ßo que Newton sugere que deve ser pago. 

Hoje √° um ressurgimento de m√©todos de primeira ordem porque problemas em ci√™ncia de dados, aprendizagem de m√°quina s√£o t√£o grandes que o uso de informa√ß√£o de segunda ordem, que √© mais dif√≠cil de calcular, n√£o √© muito pr√°tico. Al√©m disso esses problemas tem uma estrutura especial que pode ser explorada e √© favor√°vel ao uso de m√©todos de primeira ordem, principalmente de suas variantes estoc√°sticas.
